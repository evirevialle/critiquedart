		<h3 class="std__title" style="margin-top: 3px;">Evénements passés</h3>
		<h3 class="titre_actualites" id="colloque-international">Colloque international</h3>
		<h4 style="font-size: 13px;">17-19 mai 2017  (<a class="telecharger_actualites" href="/webroot/download/Colloque-17-19-mai-2017.pdf" target="_blank">manifeste <img src='/webroot/images/pdf.jpg' height='20' width='20' alt='logo PDF'></a>, 
                <a class="telecharger_actualites" href="/webroot/download/programme-mai-2017.pdf" target="_blank">programme <img src='/webroot/images/pdf.jpg' height='20' width='20' alt='logo PDF'></a>)
                </h4>
		<p class="paragraphe" style="text-align:justify" style="font-size: 13px;">
			Un colloque international avec appel à communication pour proposer les premiers résultats liés à l’exploitation du site et de la base pour de nouveaux travaux. 
            Intitulé « Une nouvelle histoire de la critique d’art à la lumière des humanités numériques ? », il permet de questionner à la fois les attendus théoriques et les résultats pratiques du site selon quatre grands axes : faire carrière, masse critique, nouveaux corpus et au-delà de la critique d’art. 
		</p>
        </div>
        <h3 class="titre_actualites" id="journee-inaugurale">Journée inaugurale</h3>
		<h4 style="font-size: 13px;">31 mars 2017 (<a class="telecharger_actualites" href="/webroot/download/BIBLIO-31MARS-2017.pdf" title="Plaquette de l'inauguration" target="new">Programme<img src='/webroot/images/pdf.jpg' height='20' width='20' alt='logo PDF'></a>)</h4>
		<p class="paragraphe" style='text-align:justify' style="font-size: 13px;">
			Une journée inaugurale du 31 mars 2017, l’occasion de présenter le programme de recherche de manière concrète au public et à la communauté scientifique. 
		</p>
        <h3 class="titre_actualites" style="font-size: 15px;">Workshop-Hackathon</h3>
        <h4 style="font-size: 13px;">3 février 2017 (<a class="telecharger_actualites" href="/webroot/download/Workshop-2017-02.pdf" title="Plaquette du WorkShop" target="new">Programme<img src='/webroot/images/pdf.jpg' height='20' width='20' alt='logo PDF'></a>)</h4>
        <p class="paragraphe"  style="text-align:justify;font-size: 13px;">
       Pour l’ouverture du site mi-février 2017, un workshop sous forme de Hackathon a été organisé par l’équipe de recherche afin de familiariser les chercheurs avec ce nouvel outil. Il s’agissait alors non seulement d’apprendre à utiliser toutes les potentialités de la base grâce à un éclairage à la fois du côté de l’histoire de l’art, de la prosopographie et des humanités numériques mais aussi de commencer à constituer des objets de recherche.
        </p>
